# CLASSES DEFINE OBJECTS

# Ruby defines the attributes and behaviors of its objects in classes.
# Classes- basic outlines of what an object should be made of and what it should do

# To Define class- similar syntax to defining a method
# - replace the (def) with (class)
# - use CamelCase
# - (end) to finish
# - Ruby file names in snake_case [only file names]

#-----------------

# EXAMPLE

# class GoodDog
# end

# sparky = GoodDog.new

# * An object is returned by calling the class method (new)
# - created an instance of GoodDog class
# - stored in the variable sparky
# - it's now an object
# - sparky is an object or instance of class GoodDog
# - we've instantiated an object called sparky from class GoodDog

# INSTANTIATION-
# - the entire workflow of creating a new object or instance from a class
